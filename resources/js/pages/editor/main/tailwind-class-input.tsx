export function TailwindClassInput({
   value,
   onChange,
   placeholder = 'e.g. flex gap-4 rounded-lg shadow-md',
   id,
   className,
}: TailwindClassInputProps) {
   const [suggestions, setSuggestions] = useState<string[]>([]);
   const [showSuggestions, setShowSuggestions] = useState(false);
   const [selectedIndex, setSelectedIndex] = useState(0);
   const [cursorPosition, setCursorPosition] = useState(0);
   const [suggestionsPosition, setSuggestionsPosition] = useState({ top: 0, left: 0, width: 0 });
   const textareaRef = useRef<HTMLTextAreaElement>(null);
   const suggestionsRef = useRef<HTMLDivElement>(null);

   useEffect(() => {
      const beforeCursor = value.substring(0, cursorPosition);
      const words = beforeCursor.split(/\s+/);
      const currentWord = words[words.length - 1] || '';

      if (currentWord.length > 0) {
         // Check if the current word contains a Tailwind modifier (e.g., md:, hover:, focus:)
         const modifierMatch = currentWord.match(
            /^(xs:|sm:|md:|lg:|xl:|2xl:|hover:|focus:|active:|disabled:|group-hover:|focus-within:|focus-visible:|dark:|first:|last:|odd:|even:|visited:|checked:|required:|invalid:|placeholder:|before:|after:)(.*)$/,
         );

         let filtered: string[];
         if (modifierMatch) {
            // If there's a modifier, filter based on the part after the modifier
            const modifier = modifierMatch[1];
            const searchTerm = modifierMatch[2];

            if (searchTerm.length > 0) {
               // Filter classes and prepend the modifier
               filtered = TAILWIND_CLASSES.filter((cls) => cls.toLowerCase().startsWith(searchTerm.toLowerCase()))
                  .map((cls) => `${modifier}${cls}`)
                  .slice(0, 15);
            } else {
               // Just typed the modifier, show common classes with that modifier
               filtered = TAILWIND_CLASSES.slice(0, 15).map((cls) => `${modifier}${cls}`);
            }
         } else {
            // No modifier, filter normally
            filtered = TAILWIND_CLASSES.filter((cls) => cls.toLowerCase().startsWith(currentWord.toLowerCase())).slice(0, 15);
         }

         // This is derived state from props, not a side effect
         // eslint-disable-next-line
         setSuggestions(filtered);
         setShowSuggestions(filtered.length > 0);
         setSelectedIndex(0);
      } else {
         setShowSuggestions(false);
         setSuggestions([]);
      }
   }, [value, cursorPosition]);

   const insertSuggestion = (suggestion: string) => {
      const beforeCursor = value.substring(0, cursorPosition);
      const afterCursor = value.substring(cursorPosition);
      const words = beforeCursor.split(/\s+/);
      words[words.length - 1] = suggestion;
      const newValue = words.join(' ') + afterCursor;

      onChange(newValue);
      setShowSuggestions(false);

      setTimeout(() => {
         if (textareaRef.current) {
            const newPosition = words.join(' ').length;
            textareaRef.current.setSelectionRange(newPosition, newPosition);
            textareaRef.current.focus();
         }
      }, 0);
   };

   const handleKeyDown = (e: React.KeyboardEvent<HTMLTextAreaElement>) => {
      if (!showSuggestions) return;

      if (e.key === 'ArrowDown') {
         e.preventDefault();
         setSelectedIndex((prev) => (prev + 1) % suggestions.length);
      } else if (e.key === 'ArrowUp') {
         e.preventDefault();
         setSelectedIndex((prev) => (prev - 1 + suggestions.length) % suggestions.length);
      } else if (e.key === 'Enter' || e.key === 'Tab') {
         if (suggestions.length > 0) {
            e.preventDefault();
            insertSuggestion(suggestions[selectedIndex]);
         }
      } else if (e.key === 'Escape') {
         setShowSuggestions(false);
      }
   };

   const handleChange = (e: React.ChangeEvent<HTMLTextAreaElement>) => {
      onChange(e.target.value);
      setCursorPosition(e.target.selectionStart);
   };

   const handleClick = (e: React.MouseEvent<HTMLTextAreaElement>) => {
      setCursorPosition(e.currentTarget.selectionStart);
      updateSuggestionsPosition();
   };

   const updateSuggestionsPosition = () => {
      if (textareaRef.current) {
         const rect = textareaRef.current.getBoundingClientRect();
         setSuggestionsPosition({
            top: rect.bottom + window.scrollY,
            left: rect.left + window.scrollX,
            width: rect.width,
         });
      }
   };

   useEffect(() => {
      if (showSuggestions) {
         updateSuggestionsPosition();
      }
   }, [showSuggestions]);

   useEffect(() => {
      const handleScroll = () => {
         if (showSuggestions) {
            updateSuggestionsPosition();
         }
      };

      window.addEventListener('scroll', handleScroll, true);
      window.addEventListener('resize', handleScroll);

      return () => {
         window.removeEventListener('scroll', handleScroll, true);
         window.removeEventListener('resize', handleScroll);
      };
   }, [showSuggestions]);

   const renderSuggestions = () => {
      if (!showSuggestions || suggestions.length === 0) return null;

      return createPortal(
         <div
            ref={suggestionsRef}
            style={{
               position: 'fixed',
               top: `${suggestionsPosition.top}px`,
               left: `${suggestionsPosition.left}px`,
               width: `${suggestionsPosition.width}px`,
            }}
            className="bg-popover text-popover-foreground z-[9999] mt-1 max-h-60 overflow-auto rounded-md border p-1 shadow-md"
         >
            {suggestions.map((suggestion, index) => (
               <div
                  key={suggestion}
                  className={cn(
                     'relative flex cursor-pointer items-center rounded-sm px-2 py-1.5 font-mono text-sm outline-none select-none',
                     index === selectedIndex ? 'bg-accent text-accent-foreground' : 'hover:bg-accent hover:text-accent-foreground',
                  )}
                  onClick={() => insertSuggestion(suggestion)}
                  onMouseEnter={() => setSelectedIndex(index)}
               >
                  {suggestion}
               </div>
            ))}
         </div>,
         document.body,
      );
   };

   return (
      <div className="relative">
         <Textarea
            ref={textareaRef}
            id={id}
            value={value}
            rows={3}
            onChange={handleChange}
            onClick={handleClick}
            onKeyDown={handleKeyDown}
            onKeyUp={(e) => setCursorPosition(e.currentTarget.selectionStart)}
            placeholder={placeholder}
         />

         {renderSuggestions()}
      </div>
   );
}
